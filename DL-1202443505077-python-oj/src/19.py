a = 0
if s <= 10:
    a = s * 0.1
elif s <= 20:
    a = 10 * 0.1 + (s - 10) * 0.075
elif s <= 40:
    a = 10 * 0.1 + 10 * 0.075 + (s - 20) * 0.05
elif s <= 60:
    a = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (s - 40) * 0.03
elif s <= 100:
    a = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + (s - 60) * 0.015
else:
    a = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + 40 * 0.015 + (s - 100) * 0.01
print(round(a,2))
    